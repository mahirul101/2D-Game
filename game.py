class Button():
    def __init__(self, x, y, image):
        self.image = image
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.pressed = False

    def draw(self):
        pressed = False
        # mouse position
        pos = pygame.mouse.get_pos()

        # if clicked, reset
        if self.rect.collidepoint(pos):
            if pygame.mouse.get_pressed()[0] == 1 and self.pressed == False:
                pressed = True
                self.pressed == True

        if pygame.mouse.get_pressed()[0] == 0:
            self.pressed = False

        display.blit(self.image, self.rect)
        return pressed


class Character():
    def __init__(self, x, y):
        self.reset(x, y)

    def update(self, lose):
        dx = 0
        dy = 0

        if lose == 0:
            key = pygame.key.get_pressed()
            if key[pygame.K_LEFT]:
                dx -= 5
            if key[pygame.K_RIGHT]:
                dx += 5
            if key[pygame.K_UP] and self.jumped == False:
                self.vel_y = -15
                self.jumped = True
            if key[pygame.K_UP] == False:
                self.jumped = False

            # gravity
            self.vel_y += 1
            if self.vel_y > 10:
                self.vel_y = 10
            dy += self.vel_y

            for tile in world.tile_list:
                # in x
                if tile[1].colliderect(self.rect.x + dx, self.rect.y, self.width, self.height):
                    dx = 0
                # in y
                if tile[1].colliderect(self.rect.x, self.rect.y + dy, self.width, self.height):
                    # if below ground
                    if self.vel_y < 0:
                        dy = tile[1].bottom - self.rect.top
                        self.vel_y = 0
                    elif self.vel_y >= 0:
                        dy = tile[1].top - self.rect.bottom
                        self.vel_y = 0

            # check for collision with bag guy
            if pygame.sprite.spritecollide(self, blob_group, False):
                lose = -1

            # check for collision with spike
            if pygame.sprite.spritecollide(self, spike_group, False):
                lose = -1

            if self.rect.bottom > 1000:
                lose = -1

            # player position update
            self.rect.x += dx
            self.rect.y += dy

            # borders
            if self.rect.left < 0:
                self.rect.left = 0
            if self.rect.right > 1000:
                self.rect.right = 1000

            # check collisoon with door
            if pygame.sprite.spritecollide(self, finish_group, False):
                lose = 1

        # player on screen
        display.blit(self.image, self.rect)

        return lose

    def reset(self, x, y):
        character = pygame.image.load('player.jpeg')
        self.image = pygame.transform.scale(character, (40, 80))
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.vel_y = 0
        self.jumped = False
        self.width = self.image.get_width()
        self.height = self.image.get_height()


class World():
    def __init__(self, data):
        self.tile_list = []
        dirt = pygame.image.load('wall.jpeg')

        row_count = 0
        for row in data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(dirt, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                if tile == 3:
                    blob = BadGuy(col_count * tile_size,
                                  row_count * tile_size + 10)
                    blob_group.add(blob)  # add enemy to blob group
                if tile == 8:
                    finish = Finish(col_count * tile_size,
                                    row_count * tile_size - (tile_size/2))
                    finish_group.add(finish)
                if tile == 2:
                    spike = Spike(
                        col_count*tile_size, row_count*tile_size + int(tile_size/2))
                    spike_group.add(spike)
                col_count += 1
            row_count += 1

    def draw(self):
        for tile in self.tile_list:
            display.blit(tile[0], tile[1])


class BadGuy(pygame.sprite.Sprite):  # draw method already in sprite class
    def __init__(self, x, y):
        pygame.sprite.Sprite.__init__(self)
        enemy = pygame.image.load('enemy.jpeg')
        self.image = pygame.transform.scale(enemy, (40, 40))
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.move = 1
        self.count = 0

    def update(self):
        self.rect.x += self.move
        self.count += 1
        if self.count > 50:
            self.move *= -1
            self.count *= -1


